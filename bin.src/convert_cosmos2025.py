    tab_ap = apTab.Table.read(path, hdu=idx_tab)

    for idx in range(len(tab_ap.columns)):
        column = tab_ap.columns[idx]
        name = column.name
        msgs = [f"tab.columns[{idx}] {name}: "]
        description = columns_tab[name]
        if description.endswith("]"):
            idx_suffix = description.rfind("[")
            name_unit = description[idx_suffix + 1 : -1]
            try:
                unit = u.Unit(name_unit)
            except ValueError:
                unit = unit_substitutes[name_unit]
                if unit is not None:
                    unit = u.Unit(unit)
            if unit is not None:
                column.unit = unit
            description = description[:idx_suffix].strip()

        columns_fix = {}
        if column.data.ndim > 1:
            description_suffixes = {}
            if description.contains(
                pattern := "in circular apertures (diameters 0.2, 0.3, 0.5,0.75, and 1)"
            ):
                diameters = ("0.2", "0.3", "0.5", "0.75", "1.0")
            elif description.contains(
                pattern := "in [0.1, 0.25, 0.5, 1.0 and 1.5] arcsecond diameter apertures"
            ):
                diameters = ("0.1", "0.25", "0.5", "1.0", "1.5")
            else:
                raise RuntimeError(f"{name} column ({description=}) has unexpected {column.data.ndim=} > 1")
            for idx_diam, diameter in enumerate(diameters):
                suffix = f"_{diameter.replace('.', 'p')}"
                name_new = f"{name}{suffix}"
                tab_ap[name_new] = column.data[:, idx_diam]
                columns_fix[name_new] = description.replace(pattern,
                                                            f"in a {diameter} arcsecond diameter aperture")
            msgs.append(f"split column {name} of shape={column.data.shape} into {len(diameters)} columns")
            del tab_ap[name]
        else:
            columns_fix[name] = description

        for name_fix, description_new in columns_fix.items():
            column = tab_ap[name_fix]
            column.description = description
            column.byteswap(inplace=True)
            column.dtype = column.dtype.newbyteorder()
            dtype = column.dtype

            if np.issubdtype(dtype, np.integer) or np.issubdtype(dtype, np.unsignedinteger):
                if (np.nanmin(column.data) == 0) and (np.nanmax(column.data) == 1) and (
                        dtype != bool): # noqa E721
                    msgs.append(f"changing flag column {name} {dtype=} to bool")
                    tab_ap[name_fix] = column.astype(bool)

            if len(msgs) > 1:
                print(msgs[0] + "; ".join(msgs[1:]))
    # This table has some column names identical to those in the first
    # these must be disambiguated
    if idx_tab == 3:
        names_new = [x.replace("_aper_", "_aper_nonhomogenized_") for x in tab_ap.colnames]
        tab_ap.rename_columns(tab_ap.colnames, names_new)

